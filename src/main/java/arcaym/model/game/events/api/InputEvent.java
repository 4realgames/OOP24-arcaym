package arcaym.model.game.events.api;

/**
 * User input events.
 */
public enum InputEvent implements Event {

    /**
     * Left input.
     */
    LEFT,

    /**
     * Right input.
     */
    RIGHT,

    /**
     * Up input.
     */
    UP,

    /**
     * Down input.
     */
    DOWN,

    /**
     * Stop input.
     */
    STOP

}
